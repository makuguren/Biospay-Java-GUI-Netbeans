    public void FetchCompletePayments(){
        try {
            // Getting Contribution DB ColumnName to Generate Query Automatically
            pst = con.prepareStatement("SELECT column_name from contributions;");
            rs = pst.executeQuery();

            StringBuilder columnData = new StringBuilder();
            while (rs.next()) {
                String columnName = rs.getString("column_name");
                columnData.append(columnName).append(" = 'PAYED' AND ");
            }
            
            String paymentQuery = columnData.toString();
            if (paymentQuery.endsWith(" AND ")) {
                paymentQuery = paymentQuery.substring(0, paymentQuery.length() - 5);
            } else {
                return; // HandleError
            }
            
            // Showing Students List if Fully Paid
            pst = con.prepareStatement("SELECT payments.student_id, users.name, users.section, users.email FROM payments INNER JOIN users ON payments.student_id = users.student_id WHERE " + paymentQuery + ";");
            rs = pst.executeQuery();

            ResultSetMetaData rss = rs.getMetaData();
            int q = rss.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTableCompPayments.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int a=1; a<=q; a++){
                    v2.add(rs.getString("student_id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("section"));
                    v2.add(rs.getString("email"));
                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CompletedPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void fetchSections(){
        try {
            pst = con.prepareStatement("SELECT section FROM sections");
            rs = pst.executeQuery();
            cbxSections.removeAllItems();
            while(rs.next()){
                cbxSections.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(CompletedPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
