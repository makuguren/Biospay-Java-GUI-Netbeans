    private void btnSaveXLSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveXLSActionPerformed
        try{
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();
           
            if(saveFile != null){
                saveFile = new File(saveFile.toString()+".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("Complete Payments");
                CellStyle centeredStyle = wb.createCellStyle();

                // Set border for the style
                centeredStyle.setBorderBottom(BorderStyle.THIN);
                centeredStyle.setBorderTop(BorderStyle.THIN);
                centeredStyle.setBorderLeft(BorderStyle.THIN);
                centeredStyle.setBorderRight(BorderStyle.THIN);

                // Set horizontal alignment to center
                centeredStyle.setAlignment(HorizontalAlignment.CENTER);

                //Create a cell for the label "ATTENDANCE" and merge cells
                Row labelRow = sheet.createRow(0);
                Cell labelCell = labelRow.createCell(0);
                labelCell.setCellValue("COMPLETE PAYMENTS");
                labelCell.setCellStyle(centeredStyle);
                sheet.addMergedRegion(new CellRangeAddress(0, 0, 0, jTableCompPayments.getColumnCount() - 1));

                // Apply borders to the merged cell containing "ATTENDANCE" label
                RegionUtil.setBorderBottom(BorderStyle.THIN, new CellRangeAddress(0, 0, 0, jTableCompPayments.getColumnCount() - 1), sheet);
                RegionUtil.setBorderTop(BorderStyle.THIN, new CellRangeAddress(0, 0, 0, jTableCompPayments.getColumnCount() - 1), sheet);
                RegionUtil.setBorderLeft(BorderStyle.THIN, new CellRangeAddress(0, 0, 0, jTableCompPayments.getColumnCount() - 1), sheet);
                RegionUtil.setBorderRight(BorderStyle.THIN, new CellRangeAddress(0, 0, 0, jTableCompPayments.getColumnCount() - 1), sheet);

                Row rowCol = sheet.createRow(1);
                for(int i=0;i<jTableCompPayments.getColumnCount();i++){
                   Cell cell = rowCol.createCell(i);
                   cell.setCellValue(jTableCompPayments.getColumnName(i));
                }

                for(int j=0;j<jTableCompPayments.getRowCount();j++){
                   Row row = sheet.createRow(j+1);
                   for(int k=0;k<jTableCompPayments.getColumnCount();k++){
                       Cell cell = row.createCell(k);
                       if(jTableCompPayments.getValueAt(j, k)!=null){
                           cell.setCellValue(jTableCompPayments.getValueAt(j, k).toString());
                       }
                   }
                }

                for (int i = 0; i < jTableCompPayments.getColumnCount(); i++) {
                   sheet.autoSizeColumn(i);
                }

                // Apply borders to all cells containing data
                for (int j = 0; j < jTableCompPayments.getRowCount(); j++) {
                    Row row = sheet.createRow(j + 1);
                    for (int k = 0; k < jTableCompPayments.getColumnCount(); k++) {
                        Cell cell = row.createCell(k);
                        if (jTableCompPayments.getValueAt(j, k) != null) {
                            cell.setCellValue(jTableCompPayments.getValueAt(j, k).toString());
                            cell.setCellStyle(centeredStyle); // Apply the style to the cell
                        }
                        // Set auto-size column for the cell
                        sheet.autoSizeColumn(k);
                    }
                }

               FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
               wb.write(out);
               wb.close();
               out.close();
               openFile(saveFile.toString());
            }else{
               JOptionPane.showMessageDialog(null,"Cancelled to Export");
            }
        }catch(FileNotFoundException e){
           System.out.println(e);
        }catch(IOException io){
           System.out.println(io);
        }
    }//GEN-LAST:event_btnSaveXLSActionPerformed

